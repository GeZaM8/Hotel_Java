    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        cust = new Customer();
        cust.setId(id);
        cust.setName(inputNama.getText());
        cust.setAddress(inputAlamat.getText());
        cust.setPhone(inputPhone.getText());
        cust.setDob(new Date(inputTanggalLahir.getDate().getTime()));
        cust.setGender(cbJenkel.getSelectedItem().toString());
        
        dao.saveCustomer(cust);
        refreshData();
        removeInput();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
       if (id == 0) {
            JOptionPane.showMessageDialog(this, "Pilih Custumer Terlebih Dahulu");
            return;
        }
        if (confirm("Apakah anda yakin ingin menghapus custumer ini?", "Hapus Room") == JOptionPane.NO_OPTION) 
            return;
        dao.deleteCustomer(id);
        refreshData();
        removeInput();
    }//GEN-LAST:event_btnHapusActionPerformed

