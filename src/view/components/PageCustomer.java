    private void tableCustomerMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCustomerMousePressed
        int selectedRow = tableCustomer.rowAtPoint(evt.getPoint());
        
        if (selectedRow != -1) {
            id = Integer.valueOf(tableCustomer.getValueAt(selectedRow, 0).toString());
            tampilData();
        }
    }//GEN-LAST:event_tableCustomerMousePressed

    public void refreshData() {
        tableCustomer.setModel(dao.getModelAllCustomer());
    }
    
    public void tampilData() {
        cust = dao.getCustomerById(id);
        inputNama.setText(cust.getName());
        inputAlamat.setText(cust.getAddress());
        inputPhone.setText(cust.getPhone());
        inputTanggalLahir.setDate(cust.getDob());
        cbJenkel.setSelectedItem(cust.getGender());
    }
    
    public void removeInput() {
        inputNama.setText("");
        inputAlamat.setText("");
        inputPhone.setText("");
        inputTanggalLahir.setDate(null);
        cbJenkel.setSelectedIndex(-1);
        
        id = 0;
    }
    
    public int confirm(String text, String title) {
        return JOptionPane.showOptionDialog(null, text, title, JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, null, null);
    }

